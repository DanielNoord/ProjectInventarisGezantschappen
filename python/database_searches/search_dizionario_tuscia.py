def search_tuscia(filename: str) -> None:
    """Checks all names in a database for hits in the Dizionario Tuscia.

    Args:
        filename: ame of initial database
    """
    with open(filename, encoding="utf-8") as file:
        persons = json.load(file)
    del persons["$schema"]

    for data in persons.values():
        if not any(i for i in data["sources"] if i.count("biografico della Tuscia")):
            surnames = {i.split(" ")[0] for i in ENTRIES}
            if data["surname"] in surnames:
                print("\n", data["name"], data["surname"])

    print("Finished checking for hits in Tuscia database!")


